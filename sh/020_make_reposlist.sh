#!/bin/bash

###########################################################
if [ $# -ne 1 ]; then
	echo "Usage: $0 reposlist_dir"
	exit 1
fi

###########################################################
dir=$1

###########################################################
find $dir -name "*.html" | while read line
do
	grep '^    <a href="/[^/]*/[^/]*">.*</a>$' $line | cut -d'"' -f2 | sed -e 's/\//	/g' | cut -f2,3
done
